async function seed() {
  console.log("Seeding weekly polls...");

  // Delete existing polls first to avoid duplicates
  const { error: deletePolls } = await supabase.from("weekly_polls").delete().neq("id", "00000000-0000-0000-0000-000000000000");
  if (deletePolls) console.log("Note: Could not clear existing polls:", deletePolls.message);

  for (const poll of weeklyPolls) {
    const { error } = await supabase.from("weekly_polls").insert(poll);
    if (error) {
      console.error(`Failed to insert poll "${poll.question_he.slice(0, 40)}...":`, error.message);
    } else {
      console.log(`  Inserted: ${poll.week_key}`);
    }
  }

  console.log("\nSeeding lab dilemmas (scenarios)...");

  // Delete existing scenarios first
  const { error: deleteScenarios } = await supabase.from("scenarios").delete().neq("id", "00000000-0000-0000-0000-000000000000");
  if (deleteScenarios) console.log("Note: Could not clear existing scenarios:", deleteScenarios.message);

  for (const d of labDilemmas) {
    const { error } = await supabase.from("scenarios").insert({
      title_he: d.title_he,
      context_he: d.context_he,
      story_he: d.story_he,
      dilemma_question_he: d.dilemma_question_he,
      choices_json: d.choices_json,
      feedback_json: d.feedback_json,
      reflection_question_he: d.reflection_question_he,
      value_conflicts_json: d.value_conflicts_json,
      closing_feedback_json: d.closing_feedback_json,
    });
    if (error) {
      console.error(`Failed to insert scenario "${d.title_he}":`, error.message);
    } else {
      console.log(`  Inserted: ${d.title_he}`);
    }
  }

  console.log("\nDone!");
}

